  splitMessage: function(text) {
    const maxLength = 200;
    const parts = [];
    
    if (text.length <= maxLength) {
      return [text];
    }
    
    let start = 0;
    while (start < text.length) {
      let end = start + maxLength;
      if (end < text.length) {
        // Try to break at a line break or space
        let lastBreak = text.lastIndexOf('\n', end);
        if (lastBreak > start) {
          end = lastBreak;
        } else {
          lastBreak = text.lastIndexOf(' ', end);
          if (lastBreak > start) {
            end = lastBreak;
          }
        }
      }
      parts.push(text.substring(start, end).trim());
      start = end;
    }
    
    return parts;
  }
